@Composable
fun TasteTrackerTheme(
    darkTheme: Boolean = true,
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = BackgroundDark.toArgb()
            window.navigationBarColor = BackgroundDark.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}

fun Modifier.glassEffect(
    cornerRadius: Dp = 16.dp,
    blurRadius: Dp = 20.dp
): Modifier {
    return if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) {
        this
            .clip(RoundedCornerShape(cornerRadius))
            .blur(blurRadius)
            .background(GlassSurfaceDark, RoundedCornerShape(cornerRadius))
            .border(1.dp, GlassBorderDark, RoundedCornerShape(cornerRadius))
    } else {
        this
            .clip(RoundedCornerShape(cornerRadius))
            .background(GlassSurfaceHighDark, RoundedCornerShape(cornerRadius))
            .border(1.dp, GlassBorderDark, RoundedCornerShape(cornerRadius))
    }
}

fun Modifier.glassSurface(
    cornerRadius: Dp = 16.dp
): Modifier {
    return this
        .clip(RoundedCornerShape(cornerRadius))
        .background(GlassSurfaceDark, RoundedCornerShape(cornerRadius))
        .border(1.dp, GlassBorderDark, RoundedCornerShape(cornerRadius))
}